let inputs = fs.readFileSync(filepath).toString().trim().split(" ").map(Number);
let num1 = inputs[0];
let num2 = inputs[1];

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(Math.floor(num1 / num2));
console.log(num1 % num2);
